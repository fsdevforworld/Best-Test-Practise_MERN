export default function formatPhoneNumberWithCarrierDomain(
  phoneNumber: string,
  carrierName: string,
  carrierCode: string,
): string {
  const emailDomain =
    PHONE_CARRIER_EMAIL_MAP.get(carrierCode) || PHONE_CARRIER_EMAIL_MAP.get(carrierName);

  if (emailDomain) {
    const phone = toNonE164Format(phoneNumber);
    return `${phone}${emailDomain}`;
  }
}
